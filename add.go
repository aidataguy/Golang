// Simple Addition program in GoLang
// Adds package main
package main

// Imports formatting 
import "fmt"

// function declared
func add(x int, y int) int {
	// function returned
	return x + y
}

// time to run the add function
func main() {
	// formatted line print to output the result of add function from above
	fmt.Println(add(50, 50))
}
